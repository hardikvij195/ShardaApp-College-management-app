package com.hvtechnologies.shardaapp;

public class Teacher_GrpNames_Spinner {


    String GrpName , GrpId ;

    public Teacher_GrpNames_Spinner(String grpName, String grpId) {

        GrpName = grpName;
        GrpId = grpId;

    }

    public String getGrpName() {
        return GrpName;
    }

    public void setGrpName(String grpName) {
        GrpName = grpName;
    }

    public String getGrpId() {
        return GrpId;
    }

    public void setGrpId(String grpId) {
        GrpId = grpId;
    }
}
