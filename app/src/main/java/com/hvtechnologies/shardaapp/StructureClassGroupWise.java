package com.hvtechnologies.shardaapp;

public class StructureClassGroupWise {

    String SchoolName , SchoolId , DepartmentName , DepartmentId , YearName , YearId , ClassName , ClassId , GroupName , GroupId ;


    public StructureClassGroupWise(String schoolName, String schoolId, String departmentName, String departmentId, String yearName, String yearId, String className, String classId, String groupName, String groupId) {
        SchoolName = schoolName;
        SchoolId = schoolId;
        DepartmentName = departmentName;
        DepartmentId = departmentId;
        YearName = yearName;
        YearId = yearId;
        ClassName = className;
        ClassId = classId;
        GroupName = groupName;
        GroupId = groupId;


    }

    public String getSchoolName() {
        return SchoolName;
    }

    public void setSchoolName(String schoolName) {
        SchoolName = schoolName;
    }

    public String getSchoolId() {
        return SchoolId;
    }

    public void setSchoolId(String schoolId) {
        SchoolId = schoolId;
    }

    public String getDepartmentName() {
        return DepartmentName;
    }

    public void setDepartmentName(String departmentName) {
        DepartmentName = departmentName;
    }

    public String getDepartmentId() {
        return DepartmentId;
    }

    public void setDepartmentId(String departmentId) {
        DepartmentId = departmentId;
    }

    public String getYearName() {
        return YearName;
    }

    public void setYearName(String yearName) {
        YearName = yearName;
    }

    public String getYearId() {
        return YearId;
    }

    public void setYearId(String yearId) {
        YearId = yearId;
    }

    public String getClassName() {
        return ClassName;
    }

    public void setClassName(String className) {
        ClassName = className;
    }

    public String getClassId() {
        return ClassId;
    }

    public void setClassId(String classId) {
        ClassId = classId;
    }

    public String getGroupName() {
        return GroupName;
    }

    public void setGroupName(String groupName) {
        GroupName = groupName;
    }

    public String getGroupId() {
        return GroupId;
    }

    public void setGroupId(String groupId) {
        GroupId = groupId;
    }

}
